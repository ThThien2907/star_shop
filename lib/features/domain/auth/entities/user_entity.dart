class UserEntity {
  final String userId;
  final String fullName;
  final String email;
  final String dob;
  final String phoneNumber;
  final String gender;
  final String role;

  UserEntity({
    required this.userId,
    required this.fullName,
    required this.email,
    required this.dob,
    required this.phoneNumber,
    required this.gender,
    required this.role,
  });
}
